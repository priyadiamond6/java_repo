package OPERATORS;

public class RelationalOperators 
{

	public static void main(String[] args) {
//		Reational Operator 
//		<     >    <=    >=   ==   !=
		
		int a = 10;
		int b = 20;
		
		System.out.println(a<b);   //  10<20   -- True
		System.out.println(a>b);   //  10>20  -- False
		System.out.println(a<=b);  //  10<=20  == True
		System.out.println(a>=b);  //  10>=20   --False
		System.out.println(a==b);  // 10==20     false
		System.out.println(a!=b);  //  10!=20     True
		
	}

}