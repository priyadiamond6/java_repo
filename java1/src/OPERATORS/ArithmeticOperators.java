package OPERATORS;

public class ArithmeticOperators 
{
	public static void main(String[] args) {

		
		int a =21 ;
		int b =7;
		
		System.out.println(a+b);   // 28
		
		System.out.println(a-b);  // 14
		
		System.out.println(a*b);   //147
		
		System.out.println(a/b);   //3
		
		System.out.println(a%b);   //0
	}

}


