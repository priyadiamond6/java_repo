package LogicalProgram;

public class MultiplyTwoNumberWithoutMultiplicationOperator {
	public static void main(String[] args) {
	int a = 10;
	int b = 20;
    int sum = 0;
    for (int i=1; i<=b; i++) { // i<=b 
    	                      // 1<=20
    	sum = sum+a; // sum =10
    // 10(sum) = 0+10 = 10
    //    = 10+10 = 20
    // 
    
    }
	System.out.println(sum);
	}
}
