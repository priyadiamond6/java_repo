package package1;

public class class7 {

	public static void main(String[] args) {
		// Assignment Operators
		 int a=10;
		 System.out.println(a);
		  
		 // a value is taken as its answer in previous line
		 
		 a+=10;
		 System.out.println(a); // operator a=a+10, a=20
		 
		 a-=10;
		 System.out.println(a); // operator a=a-10, a=20-10, a=10
		 
		 a*=10;
		 System.out.println(a); // operator a=a*10, a=10*10, a=100
		 
		 a/=10;
		 System.out.println(a); // operator a=a/10, a=100/10, a=10
		 
		 a%=10;
		 System.out.println(a); // operator a=a%10, a=10%10, a=0 (reminder after after division) 
		 
		 a&=10;
		 System.out.println(a); // operator a=0&10, a=0
		 
		 a|=10;
		 System.out.println(a); // operator a=0|10, a=10
		 
		 a^=10;
		 System.out.println(a); // operator a=10^10, a=0
		 
		 a>>=3;
		 System.out.println(a); // operator a=0>>10, a=0
		 
		 a<<=10;
		 System.out.println(a); // operator a=0<<10, a=0
		 
	}

}
